""" Basic arithmetic operations """

class Operations(object):
    """ Basic arithmetic operations """

    def addition(self, a, b):
        """ Adds b to a """
        return a + b


    def subtraction(self, a, b):
        """ Subtracts b from a """
        return a - b


    def multiply(self, a, b):
        """ Multiplies a * b """
        return a * b


    def divide(self, a, b):
        """ Divides float(a) / b """
        return float(a) / b
